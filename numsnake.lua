numspace=256

cls()



for x=0,16 do
    for y=0,16 do
        spr(1,x*8,y*8)
    end
end

arr = {}

for i=(minnum+1),numspace do
    arr[i] = i-1
end

function shuffle(tbl)
  local size = #tbl
  for i = size, 1, -1 do
    local rand = flr(rnd(size)) + 1
    tbl[i], tbl[rand] = tbl[rand], tbl[i]
  end
  return tbl
end

shuffle(arr)

local count = 1
for x=0,15 do
    for y=0,15 do
        printh(count)
        if arr[count] < maxnum then
            print(arr[count],x*8,y*8+1,9)
        end

        count=count+1
    end
end

